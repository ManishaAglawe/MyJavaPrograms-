package String_Functions;

public class Upper_Lower_Length
{

	public static void main(String[] args)
	{
      String name="manisha";
      int Length_of_string=name.length();//to find length of name
      
      System.out.println(Length_of_string); 
      String a1=name.toUpperCase(); // to make a1 in capital letter
      
      System.out.println(a1);
      String a2=a1.toLowerCase(); //to make a1 in lowercase letter
      System.out.println(a2);
      
      
}
}
